function getAccentTrees(n) {
	if (n === undefined) {
		return getAccentTrees(2).concat(getAccentTrees(3), getAccentTrees(4));
	}
    var c = 2**n;
    var res = [];
	var suffix = 'One,Two,Three,Four'.split(',')[n-1];
    for (var i = 0; i < c; i++) {
        var partials = [];
		for (var j = n-1; j >= 0; j--)
			partials.push(((i >> j) & 1) ? 'a' : 'u');
        partials.push(suffix);
        var name = partials.join('');
		var tree = window[name];
		res.push(tree);
    }
	return res;
}